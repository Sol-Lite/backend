CREATE INDEX idx_fxorders_requested   ON fx_orders (requested_at);


-- ----------------------------------------
-- 12. 대시보드 / 위젯
-- ----------------------------------------

CREATE TABLE dashboards (
    dashboard_id    NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id         NUMBER(19,0)   NOT NULL,
    dashboard_name  VARCHAR2(100),
    page_order      NUMBER(5,0)    NOT NULL,
    default_yn      CHAR(1)        DEFAULT 'N' NOT NULL,
    created_at      TIMESTAMP      NOT NULL,
    updated_at      TIMESTAMP      NOT NULL,
    CONSTRAINT uq_dashboards_user_order UNIQUE (user_id, page_order),
    CONSTRAINT fk_dashboards_user       FOREIGN KEY (user_id) REFERENCES users (user_id)
);


CREATE TABLE widget_layouts (
    widget_layout_id NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dashboard_id     NUMBER(19,0)   NOT NULL,
    widget_type      VARCHAR2(50)   NOT NULL,
    position_x       NUMBER(5,0)    NOT NULL,
    position_y       NUMBER(5,0)    NOT NULL,
    width            NUMBER(5,0)    NOT NULL,
    height           NUMBER(5,0)    NOT NULL,
    config_json      CLOB,
    created_at       TIMESTAMP      NOT NULL,
    updated_at       TIMESTAMP      NOT NULL,
    CONSTRAINT fk_wlayouts_dashboard FOREIGN KEY (dashboard_id) REFERENCES dashboards (dashboard_id)
);

CREATE INDEX idx_wlayouts_dashboard ON widget_layouts (dashboard_id);


-- ----------------------------------------
-- 13. 관심종목
-- ----------------------------------------

CREATE TABLE watchlist_items (
    watchlist_item_id NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id           NUMBER(19,0)   NOT NULL,
    instrument_id     NUMBER(19,0)   NOT NULL,
    display_order     NUMBER(5,0),
    created_at        TIMESTAMP      NOT NULL,
    CONSTRAINT uq_watchlist_user_inst UNIQUE (user_id, instrument_id),
    CONSTRAINT fk_watchlist_user       FOREIGN KEY (user_id)       REFERENCES users (user_id),
    CONSTRAINT fk_watchlist_instrument FOREIGN KEY (instrument_id) REFERENCES instruments (instrument_id)
);

CREATE INDEX idx_watchlist_user ON watchlist_items (user_id);


-- ----------------------------------------
-- 14. 가격 알림
-- ----------------------------------------

CREATE TABLE price_alerts (
    price_alert_id    NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id           NUMBER(19,0)   NOT NULL,
    instrument_id     NUMBER(19,0)   NOT NULL,
    alert_type        VARCHAR2(20)   NOT NULL,
    threshold_percent NUMBER(5,2),
    target_price      NUMBER(19,4),
    direction         VARCHAR2(10),
    active_yn         CHAR(1)        DEFAULT 'Y' NOT NULL,
    triggered_at      TIMESTAMP,
    created_at        TIMESTAMP      NOT NULL,
    CONSTRAINT fk_palerts_user       FOREIGN KEY (user_id)       REFERENCES users (user_id),
    CONSTRAINT fk_palerts_instrument FOREIGN KEY (instrument_id) REFERENCES instruments (instrument_id)
);

CREATE INDEX idx_palerts_user      ON price_alerts (user_id);
CREATE INDEX idx_palerts_user_inst ON price_alerts (user_id, instrument_id);


-- ----------------------------------------
-- 15. 알림
-- ----------------------------------------

CREATE TABLE notifications (
    notification_id   NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id           NUMBER(19,0)   NOT NULL,
    notification_type VARCHAR2(30)   NOT NULL,
    title             VARCHAR2(200)  NOT NULL,
    message           VARCHAR2(1000),
    reference_type    VARCHAR2(30),
    reference_id      VARCHAR2(100),
    read_yn           CHAR(1)        DEFAULT 'N' NOT NULL,
    read_at           TIMESTAMP,
    created_at        TIMESTAMP      NOT NULL,
    CONSTRAINT fk_notifications_user FOREIGN KEY (user_id) REFERENCES users (user_id)
);

CREATE INDEX idx_notif_user         ON notifications (user_id);
CREATE INDEX idx_notif_user_read    ON notifications (user_id, read_yn);
CREATE INDEX idx_notif_created      ON notifications (created_at);


-- ----------------------------------------
-- 16. 알림 설정
-- ----------------------------------------

CREATE TABLE notification_settings (
    notification_setting_id   NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id                   NUMBER(19,0)   NOT NULL,
    price_alert_enabled_yn    CHAR(1)        DEFAULT 'Y' NOT NULL,
    execution_alert_enabled_yn CHAR(1)       DEFAULT 'Y' NOT NULL,
    ai_alert_enabled_yn       CHAR(1)        DEFAULT 'Y' NOT NULL,
    default_threshold_percent  NUMBER(5,2)   DEFAULT 5.00,
    updated_at                TIMESTAMP      NOT NULL,
    CONSTRAINT uq_notifsettings_user UNIQUE (user_id),
    CONSTRAINT fk_notifsettings_user FOREIGN KEY (user_id) REFERENCES users (user_id)
);
